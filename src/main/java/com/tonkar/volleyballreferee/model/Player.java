package com.tonkar.volleyballreferee.model;

public class Player {

    private int num;
    private int pos;

    public Player() {}

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public int getPos() {
        return pos;
    }

    public void setPos(int pos) {
        this.pos = pos;
    }
}
